package learning.components;

public class DonkeyWife {
    private String name;

    public DonkeyWife() {
    }

    public DonkeyWife(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "DonkeyWife{" +
                "name='" + name + '\'' +
                '}';
    }
}
